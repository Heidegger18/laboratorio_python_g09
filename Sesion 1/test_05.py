
"""Tipos de datos transcendentales"""

"""Tipo de datos: Lista []"""
var1 = [20, "Luis", "Calle Las Almendras", 1993]
print("El valor de mi variable var1 es {}".format(var1))


"""Tipo de datos: Tupla ()"""
var2 = (40, 80.9, 170, 2024, "Hola Pythonista")
print("El valor de mi variable v2 es como tupla {}".format(var2))


"""Tipo de datos: Set {}"""
var3 = set(["Bienvenidos", 50, 150, 40.79, False, 100, 50, 50, 150])
print("El valor de mi variable v3 es como conjunto {}".format(var3))


"""Tipo de datos diccionario: {}"""
var4 = {"nombre": "Mario", "edad":20, "dni": 12547885}
print("El valor de mi variable var4 es como diccionario {}".format(var4))





