
"""Listas en Python"""

"""Listas pop(): Quita un elemento de una posición dada"""

lista = ["Perú", "España", "Cánada", "Argentina"]

print("Mi lista inicial es la siguiente: {}".format(lista))

lista.pop() #última posición
print("Mi lista actualizada es: {}".format(lista))

lista.pop(1)
print("Mi lista actualizada es: {}".format(lista))